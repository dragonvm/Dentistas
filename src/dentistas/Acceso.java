    private void boton_entrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_boton_entrarMouseClicked
        String usuario="admin", contrasenya="admin";

        String nameuser = new String(user.getText());
        String password = new String(pass.getPassword());
        if(usuario.equals(nameuser)&&password.equals(contrasenya)){
            Principal abrir=new Principal();
            abrir.setVisible(true);
            dispose();

        }else if(usuario.equals(nameuser)&&!password.equals(contrasenya)){
            JOptionPane.showMessageDialog(this, "Contrase単a incorrecta", "Contrase単a incorrecta", ERROR_MESSAGE);
        }else if(!usuario.equals(nameuser)&&password.equals(contrasenya)){
            JOptionPane.showMessageDialog(this, "Usuario incorrecto", "Usuario incorrecto", ERROR_MESSAGE);
        }else {
            JOptionPane.showMessageDialog(this, "Datos Incorrectos", "Usuario y Contrase単a son incorrectos", ERROR_MESSAGE);
        }

    }//GEN-LAST:event_boton_entrarMouseClicked

